   function lookup_situacao(&$situacao) 
   {
      $conteudo = "" ; 
      if ($situacao == "A")
      { 
          $conteudo = "Ativo";
      } 
      if ($situacao == "I")
      { 
          $conteudo = "Inativo";
      } 
      if ($situacao == "S")
      { 
          $conteudo = "Suspenso";
      } 
      if (!empty($conteudo)) 
      { 
          $situacao = $conteudo; 
      } 
   }  
